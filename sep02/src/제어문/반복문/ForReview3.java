package 제어문.반복문;

import java.util.Scanner;

public class ForReview3 {

	public static void main(String[] args) {

		
		Scanner	sc = new Scanner(System.in);
		int p1 = 0;
		int p2 = 0;
		int p3 = 0;
		
		while (true) {
			System.out.print("입력 1)아이유 2)유재석 3)방탄 4)종료>> ");
			int pick = sc.nextInt();

			if (pick == 1) {
				p1++;
			} else if (pick == 2) {
				p2++;
			} else if (pick == 3) {
				p3++;
			} else {
				System.out.println("종료합니다");
				System.out.println("아이유 득표수>> " + p1+"표");
				System.out.println("유재석 득표수>> " + p2+"표");
				System.out.println("방탄 득표수>> " + p3+"표");
				System.exit(0);
			} 
		}
	}
}
